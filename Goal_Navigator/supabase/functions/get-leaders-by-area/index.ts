import { createClient } from "https://esm.sh/@supabase/supabase-js@2.49.1";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const { area } = await req.json();
    if (!area) {
      return new Response(JSON.stringify({ error: "area is required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const supabaseAdmin = createClient(
      Deno.env.get("SUPABASE_URL")!,
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!
    );

    // Query leader_directory for leaders in the selected area + CEO (EXECUTIVE area appears everywhere)
    const { data: leaders, error } = await supabaseAdmin
      .from("leader_directory")
      .select("full_name, cargo, area, subarea, email")
      .or(`area.eq.${area},area.eq.EXECUTIVE`);

    if (error) throw error;

    return new Response(JSON.stringify(leaders || []), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (err) {
    return new Response(JSON.stringify({ error: err instanceof Error ? err.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
